/*
    loops in java
    -   for 
    -   while 


*/

public class loop {
    public static void main(String[] args) {
        System.out.println("For loop :");
        for (int i = 0; i < 11; i++) {
            System.out.println(i);
        }
        System.out.println("");
        System.out.println("While loop :");
        int i=1;
        while (i<5) {
            System.out.println("github.com/Hunter-420");
            i++;
        }
        System.out.println("");
        System.out.println("do while loop :");
        i=1;
        do {
            System.out.println("nischal.khanal69");
            i++;
        } while (i<5);
    }
}
